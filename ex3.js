//Faça um algoritmo que peça dois números e imprima o maior deles.

let numero1 = parseInt(prompt("Informe o Primeiro Número:"));
let numero2 = parseInt(prompt("Informe o Segundo Número:"));

if (numero1 > numero2) {
    alert("O primeiro número é maior que o segundo.");
} else {
    alert("O segundo número é maior que o primeiro.");
}